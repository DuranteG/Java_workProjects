package squadreItalia_Natale;

class Team{
    String name;
    String region;
    String city;
    String sponsor;
    String coach;
    String championships;
    String stadium;
    String website;
    
    Team(String name, String region, String city, String sponsor, String coach, String championships, String stadium, String website){
        this.name = name;
        this.region = region;
        this.city = city;
        this.sponsor = sponsor;
        this.coach = coach;
        this.championships = championships;
        this.stadium = stadium;
        this.website = website;
    }
}